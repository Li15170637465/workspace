#include <stdio.h>
/***********************************************************************
*   赋值运算符:
     =:  赋值号(把等号右边的数据赋值给等号左右的变量)


    算术运算符|位运算符  和赋值号结合:  简化赋值计算的

    完整的计算表达式:  目标数=目标数（运算符）操作数
    简化写法:  目标数运算符=操作数  比如:  a*=1;

***********************************************************************/
int main(){
    int a=10;
   // a=a+1;
    a+=1;
   // a=a+10;
    a+=10;

    a=a*5;
    a*=5;


    a=a<<2;
    a<<=2;
    return 0;
}